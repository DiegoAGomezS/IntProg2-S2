print("Si desea conocer el antecesor y sucesor de un número por favor ingreselo: ")
numero = int(input("Coloque aquí el número "))

antecesor = numero - 1
sucesor = numero + 1

print("El antecesor del número que proporciono es: ", antecesor)
print("El sucesor del número que proporciono es: ", sucesor)